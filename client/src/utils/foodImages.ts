// 카테고리에 따른 이미지 반환 함수
export const getImageForCategory = (category: string, index: number = 0): string => {
  const normalizedCategory = category.toLowerCase();
  
  // 카테고리 매핑
  if (normalizedCategory.includes('한식') || normalizedCategory.includes('한국')) {
    const images = foodImages.korean;
    return images[index % images.length];
  }
  if (normalizedCategory.includes('중식') || normalizedCategory.includes('중국') || normalizedCategory.includes('짜장')) {
    const images = foodImages.chinese;
    return images[index % images.length];
  }
  if (normalizedCategory.includes('일식') || normalizedCategory.includes('일본') || normalizedCategory.includes('스시') || normalizedCategory.includes('초밥')) {
    const images = foodImages.japanese;
    return images[index % images.length];
  }
  if (normalizedCategory.includes('양식') || normalizedCategory.includes('이탈') || normalizedCategory.includes('파스타') || normalizedCategory.includes('피자')) {
    const images = foodImages.italian;
    return images[index % images.length];
  }
  if (normalizedCategory.includes('카페') || normalizedCategory.includes('커피') || normalizedCategory.includes('디저트') || normalizedCategory.includes('베이커리')) {
    const images = foodImages.cafe;
    return images[index % images.length];
  }
  if (normalizedCategory.includes('치킨') || normalizedCategory.includes('호프') || normalizedCategory.includes('맥주')) {
    const images = foodImages.chicken;
    return images[index % images.length];
  }
  if (normalizedCategory.includes('분식') || normalizedCategory.includes('떡볶이') || normalizedCategory.includes('김밥')) {
    const images = foodImages.snack;
    return images[index % images.length];
  }
  if (normalizedCategory.includes('해산물') || normalizedCategory.includes('회') || normalizedCategory.includes('횟집')) {
    const images = foodImages.seafood;
    return images[index % images.length];
  }
  if (normalizedCategory.includes('베트남') || normalizedCategory.includes('태국') || normalizedCategory.includes('쌀국수') || normalizedCategory.includes('동남아')) {
    const images = foodImages.asian;
    return images[index % images.length];
  }
  if (normalizedCategory.includes('고기') || normalizedCategory.includes('구이') || normalizedCategory.includes('삼겹살') || normalizedCategory.includes('갈비')) {
    const images = foodImages.bbq;
    return images[index % images.length];
  }
  
  // 기본값 - 한식
  return foodImages.korean[index % foodImages.korean.length];
};

// 랜덤 이미지 반환
export const getRandomFoodImage = (category?: string): string => {
  if (category) {
    const randomIndex = Math.floor(Math.random() * 5);
    return getImageForCategory(category, randomIndex);
  }
  
  // 카테고리가 없으면 모든 이미지 중에서 랜덤
  const allCategories = Object.keys(foodImages);
  const randomCategory = allCategories[Math.floor(Math.random() * allCategories.length)];
  const images = foodImages[randomCategory as keyof typeof foodImages];
  return images[Math.floor(Math.random() * images.length)];
};

// 플레이리스트용 이미지
export const getPlaylistImage = (title: string): string => {
  const titleLower = title.toLowerCase();
  
  if (titleLower.includes('데이트') || titleLower.includes('연인')) {
    return 'https://images.unsplash.com/photo-1555396273-367ea4eb4db5?w=800';
  }
  if (titleLower.includes('혼밥')) {
    return 'https://images.unsplash.com/photo-1498654896293-37aacf113fd5?w=800';
  }
  if (titleLower.includes('회식')) {
    return 'https://images.unsplash.com/photo-1544025162-d76694265947?w=800';
  }
  if (titleLower.includes('브런치')) {
    return 'https://images.unsplash.com/photo-1488477181946-6428a0291777?w=800';
  }
  
  return getRandomFoodImage();
};