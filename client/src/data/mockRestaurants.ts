export const searchRestaurants = (query: string): Restaurant[] => {
  if (!query.trim()) return mockRestaurants;
  
  const lowerQuery = query.toLowerCase();
  return mockRestaurants.filter(restaurant => {
    // Find original mock data for searching
    const originalMock = allMockRestaurants.find(mock => mock.id === restaurant._id);
    return restaurant.name.toLowerCase().includes(lowerQuery) ||
           restaurant.category.toLowerCase().includes(lowerQuery) ||
           restaurant.address.toLowerCase().includes(lowerQuery) ||
           (originalMock?.area.toLowerCase().includes(lowerQuery)) ||
           (originalMock?.specialties.some(specialty => 
             specialty.toLowerCase().includes(lowerQuery)
           ));
  });
};

